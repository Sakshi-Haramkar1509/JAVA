public class Argument {
    static void star (int n)
    {
        int num = n;
        for(int i = 1; i <= num; i++)
        {
            System.out.print("*");

        }
        System.out.println();

    }
    public static void main(String args[]){
        star (10);
        System.out.println("VKCL");
        star (20);
        System.out.println("Shegaon");
        star (30);
        System.out.println("Buldhana");
        star (40);
    } 
    
}
